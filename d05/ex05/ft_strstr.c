char	*ft_strstr(char *str, char *to_find)
{
	int		i;
	int		y;
	char	*in;

	if (to_find[0] == '\0')
		return (str);
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == to_find[0])
		{
			in = str + i;
			y = 0;
			while (str[i + y] == to_find[y])
			{
				if (to_find[y + 1] == '\0')
					return (in);
				y++;
			}
			in = 0;
		}
		i++;
	}
	return (0);
}
