int		ft_str_is_lowercase(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if (!(c >= 97 && c <= 122))
			return (0);
		i++;
	}
	return (1);
}
