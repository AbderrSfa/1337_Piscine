char	*ft_strcat(char *dest, char *src)
{
	int i;
	int l;

	i = 0;
	l = 0;
	while (dest[i] != '\0')
	{
		l++;
		i++;
	}
	i = 0;
	while (src[i] != '\0')
	{
		dest[l + i] = src[i];
		i++;
	}
	dest[l + i] = '\0';
	return (dest);
}
